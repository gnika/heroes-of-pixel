var map = {
    cols: 48,
    rows: 48,
    tsize: 64,
    layers: [abs1, abs2],
    getTile: function (layer, col, row) {
        return this.layers[layer][row * map.cols + col];
    },
    isLoseWinLife: function (x, y) {
        var col = Math.floor(x / this.tsize);
        var row = Math.floor(y / this.tsize);
 
 
        return this.layers.reduce(function (res, layer, index) {
            var tile = this.getTile(index, col, row);
			if (tile==14)//fontaine
				return 'life';
			if(tile==15)//sable
				Hero.SPEED = 80;
			else
				Hero.SPEED = 256;
			
            var isSolid = tile === 8 || tile === 9;
            return res || isSolid;
        }.bind(this), false);
    },
    isSolidTileAtXY: function (x, y) {
        var col = Math.floor(x / this.tsize);
        var row = Math.floor(y / this.tsize);
        // tiles 3 and 5 6 and 7 are solid -- the rest are walkable
        // loop through all layers and return TRUE if any tile is solid
        return this.layers.reduce(function (res, layer, index) {
            var tile = this.getTile(index, col, row);
			// console.log(builds, row, 1+col);
            var isSolid = tile === 3 || tile === 5 || tile === 16 || tile === 17;
			if(builds[col+'-'+row] && builds[col+'-'+row].solid==1)
				var isSolid=1;
			if(isSolid){
				clickCanvasX = 0;
				clickCanvasY = 0;
			}
			
            return res || isSolid;
        }.bind(this), false);
    },
    isEnnemyTileAtXY: function (x, y) {
			var row = Math.floor(x / this.tsize);
			var col = Math.floor(y / this.tsize);
			var isSolid=0;
			if(monsters[row+'-'+col])
				isSolid = monsters[row+'-'+col];
				

			if(isSolid){
				clickCanvasX = 0;
				clickCanvasY = 0;
			}
			
			return isSolid;
    },
    isHeroTileAtXY: function (hx, hy) {
			var hrow = Math.floor(hx / this.tsize);
			var hcol = Math.floor(hy / this.tsize);
			return [hrow, hcol];
    },
    getCol: function (x) {
        return Math.floor(x / this.tsize);
    },
    getRow: function (y) {
        return Math.floor(y / this.tsize);
    },
    getX: function (col) {
        return col * this.tsize;
    },
    getY: function (row) {
        return row * this.tsize;
    }
};